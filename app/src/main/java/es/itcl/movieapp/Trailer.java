public class Trailer {
    private String id;
    private String key;
    private String name;

    public String getID() {
        return id;
    }
    public void setID(String id) {
        this.id = id;
    }

    public String getKey() {
        return key;
    }
    public void setKey(String key) {
        this.key = key;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

}
